    private void jButGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButGrabarActionPerformed
        Empleado x = new Empleado();
        x.setNombre(jTextNombre.getText());
        x.setDiasTrabajados(Integer.parseInt(jTextDias.getText()));
        x.setSueldoDiario(Double.parseDouble(jTextSueldo.getText()));
        x.setHorasExtra(Integer.parseInt(jTextHoras.getText()));
        x.setPorcentajeImpuesto(Double.parseDouble(jTextImpuesto.getText()));
        
        
        int posicion = (int)jSpinPosicion.getValue()-1;
        
        nomina[posicion] = x;
        
        mostrar();
        if(jCheckBox1.isSelected())
            if(posicion==nomina.length-1)
                jSpinPosicion.setValue(1);
        else
            jSpinPosicion.setValue(posicion+2);
        
        
    }//GEN-LAST:event_jButGrabarActionPerformed

