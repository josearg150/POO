    public void mostrar(){
        DecimalFormat d = new DecimalFormat("#,###,###.00");
        double total = 0;
        
         for(int r=0; r<nomina.length; r++){
            jTabNomina.setValueAt(nomina[r].getNombre(), r, 0);
            jTabNomina.setValueAt(nomina[r].getDiasTrabajados(), r, 1);
            jTabNomina.setValueAt(d.format(nomina[r].getSueldoDiario()), r, 2);
            jTabNomina.setValueAt(nomina[r].getHorasExtra(), r, 3);
            jTabNomina.setValueAt(nomina[r].getPorcentajeImpuesto(), r, 4);
            
            jTabNomina.setValueAt(d.format(nomina[r].importeHorasExtras()), r, 5);
            jTabNomina.setValueAt(d.format(nomina[r].importeImpuesto()), r, 6);
            jTabNomina.setValueAt(d.format(nomina[r].sueldo()), r, 7);
            
            total += nomina[r].sueldo();
      }
         jLabTotal.setText(d.format(total));
    }

