    public double volumenCilindro(double radio, double altura){
        double volumen=0;
        volumen = Math.PI * Math.pow(radio, 2) * altura;
        return volumen;
    }
    public double areaCilindro(double radio, double altura){
        return 2 * Math.PI * radio * (radio+altura);
    }
